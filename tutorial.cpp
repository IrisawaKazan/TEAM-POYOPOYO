CTutorial::CTutorial()
{
	m_type = TUTORIAL_NOEN;	// チュートリアルの種類
	m_fWidth = 0.0f;		// 横幅
	m_fHeigth = 0.0f;		// 縦幅
}

//***************************************
// デストラクタ
//***************************************
CTutorial::~CTutorial()
{
}

//***************************************
// 初期化処理
//***************************************
HRESULT CTutorial::Init(void)
{
	m_type = TUTORIAL_NOEN;	// タイプをなしに設定
	m_fWidth = 0.0f;		// 横幅
	m_fHeigth = 0.0f;		// 縦幅

	return S_OK;
}

